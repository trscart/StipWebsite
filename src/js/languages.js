$(".lang").text("");
$(document).ready(function () {
    let language = null
    if (sessionStorage.getItem('language') == null) {
        language = navigator.language; // get current language
    }

    if ($(location).attr('href').includes("digital-customer-service-guide")) { // only english version for lead magnet page
        language = "en-EN";
        sessionStorage.setItem('language', "en-EN");
    }

    // change text language on load
    if ((language != "it-IT" && language != null) || (sessionStorage.getItem('language') != "it-IT" && sessionStorage.getItem('language') != null)) { //if navigator.language and "language" item is both different from it-IT
        $('a[blog]').attr("href", "https://stip.io/en/blog/") // change href for blog link
        $(".lang").each(function () {
            $(this).text(languages["en-EN"][$(this).attr("key")]);
            $(".stip-enBtn").css("color", "#399fad")
        });
        $(".placeholderLang").each(function () {
            $(this).attr("placeholder", languages["en-EN"][$(this).attr("key")]);
        });
        $(".datacontentLang").each(function () {
            $(this).attr("data-content", languages["en-EN"][$(this).attr("key")]);
        });
        // get latest articles and append them on footer
        fetch('https://stip.io/en/blog/api/articles/latests/', {
            mode: "cors",
            method: 'GET',
            headers: {
                'content-type': 'application/json'
            }
        })
            .then(function (res) {
                res.json().then(function (data) {
                    //console.log(data)
                    for (let i = 0; i < 3; i++) {
                        $(".stip-footerBlog").append("<a href='https://stip.io/en/blog/" + data[i].slug + "' title='blog' class='stip-txt'>" + data[i].draft_title.split(' ').slice(0, 6).join(' ').concat(" ...") + "</a>");
                    }
                })
            })
            .catch(function (err) { //if error
                //console.log(err)
            })
    } else {
        $('a[blog]').attr("href", "https://stip.io/it/blog/") // change href for blog link
        $(".lang").each(function () {
            $(this).text(languages["it-IT"][$(this).attr("key")]);
            $(".stip-itBtn").css("color", "#399fad")
        });
        $(".placeholderLang").each(function () {
            $(this).attr("placeholder", languages["it-IT"][$(this).attr("key")]);
        });
        $(".datacontentLang").each(function () {
            $(this).attr("data-content", languages["it-IT"][$(this).attr("key")]);
        });
        // get latest articles and append them on footer
        fetch('https://stip.io/it/blog/api/articles/latests/', {
            mode: "cors",
            method: 'GET',
            headers: {
                'content-type': 'application/json'
            }
        })
            .then(function (res) {
                res.json().then(function (data) {
                    //console.log(data)
                    for (let i = 0; i < 3; i++) {
                        $(".stip-footerBlog").append("<a href='https://stip.io/it/blog/" + data[i].slug + "' title='blog' class='stip-txt'>" + data[i].draft_title.split(' ').slice(0, 6).join(' ').concat(" ...") + "</a>");
                    }
                })
            })
            .catch(function (err) { //if error
                //console.log(err)
            })
        $("a[title='platform']").hide()
    }

    // change text language on click
    $(".stip-itBtn").click(function (e) { // click for it-IT language
        sessionStorage.setItem('language', "it-IT"); // set language in session storage item
        $(".stip-itBtn").css("color", "#399fad")
        $(".stip-enBtn").css("color", "#303030")
        if ($(location).attr('href').includes("privacy-policy")) { // stip policy language
            window.location = "privacy-policy.html"
        } else {
            location.reload(); // reload to change eng changes
        }
    });

    $(".stip-enBtn").click(function (e) { // click for en-EN language
        sessionStorage.setItem('language', "en-EN"); // set language in session storage item
        $(".stip-itBtn").css("color", "#303030")
        $(".stip-enBtn").css("color", "#399fad")
        if ($(location).attr('href').includes("privacy-policy")) { // stip policy language
            window.location = "privacy-policy-en.html"
        } else {
            location.reload(); // reload to change eng changes
        }
    });

    // stip policy language
    $("#stip-policy").click(function () {
        if (sessionStorage.getItem('language') == "en-EN" || (navigator.language != "it-IT" && sessionStorage.getItem('language') == null)) {
            $(this).attr("href", "./privacy-policy-en.html");
        } else {
            $(this).attr("href", "./privacy-policy.html");
        }
    });
})
